
package modelo;

public class Unidad {
    private int idUnidad ;
    private String nombreProductoUnidad;
    private String codigoBarras;
    private int cantidadUnidades;
    private float  precioUnidad;
    private int idProducto;

    public int getIdUnidad() {
        return idUnidad;
    }

    public void setIdUnidad(int idUnidad) {
        this.idUnidad = idUnidad;
    }

    public String getNombreProductoUnidad() {
        return nombreProductoUnidad;
    }

    public void setNombreProductoUnidad(String nombreProductoUnidad) {
        this.nombreProductoUnidad = nombreProductoUnidad;
    }

    public String getCodigoBarras() {
        return codigoBarras;
    }

    public void setCodigoBarras(String codigoBarras) {
        this.codigoBarras = codigoBarras;
    }

    public int getCantidadUnidades() {
        return cantidadUnidades;
    }

    public void setCantidadUnidades(int cantidadUnidades) {
        this.cantidadUnidades = cantidadUnidades;
    }

    public float getPrecioUnidad() {
        return precioUnidad;
    }

    public void setPrecioUnidad(float precioUnidad) {
        this.precioUnidad = precioUnidad;
    }

    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }
    
    
}
